import java.util.Scanner;
 
public class RelationalOperators {
  public static void main(String[] args) {
    System.out.println("num1 > num2 is " + (num1 > num2));
    System.out.println("num1 < num2 is " + (num1 < num2));
    System.out.println("num1 >= num2 is " + (num1 >= num2));
    System.out.println("num1 <= num2 is " + (num1 <= num2));
    System.out.println("num1 == num2 is " + (num1 == num2));
    System.out.println("num1 != num2 is " + (num1 != num2));
  }
}